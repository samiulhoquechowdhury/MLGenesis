# types of operator

#Arithmetic operator
a = 5
b = 4

print(a + b)
print(a - b)
print(a * b)
print(a / b) #Division (float)
print(a % b) #Modulus (Rreminder)
print(a ** b) #Exponential a ** b ==> 625



#Relational Operator/ Comparision Operator
# (   == , != , > , < , <= , >=   )


c = 7 
d = 8
print(c == d)  #False
print(c != d)  #True
print(c > d)   #False
print(c < d)   #Ture
print(c <= d)  #True
print(c >= d)  #False



#Assignment Operator
# (   = , += , -= , *= , /= , %= , **=   )
